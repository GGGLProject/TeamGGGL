package com.sist.match.dao;

public class PlayerVO {
	private int player_no;
	private String player_team;
	private String player_name;
	private String player_pic;
	
	public int getPlayer_no() {
		return player_no;
	}
	public void setPlayer_no(int player_no) {
		this.player_no = player_no;
	}
	public String getPlayer_team() {
		return player_team;
	}
	public void setPlayer_team(String player_team) {
		this.player_team = player_team;
	}
	public String getPlayer_name() {
		return player_name;
	}
	public void setPlayer_name(String player_name) {
		this.player_name = player_name;
	}
	public String getPlayer_pic() {
		return player_pic;
	}
	public void setPlayer_pic(String player_pic) {
		this.player_pic = player_pic;
	}
	
	

}
