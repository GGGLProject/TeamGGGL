package com.sist.match.dao;

public class TeamVO {
	private String team_name;
	private String team_icon;
	private String team_league;
	
	public String getTeam_name() {
		return team_name;
	}
	public void setTeam_name(String team_name) {
		this.team_name = team_name;
	}
	public String getTeam_icon() {
		return team_icon;
	}
	public void setTeam_icon(String team_icon) {
		this.team_icon = team_icon;
	}
	public String getTeam_league() {
		return team_league;
	} 
	public void setTeam_league(String team_league) {
		this.team_league = team_league;
	}
	

}
